function useSingleTap(onTap) {
  const touchedRef = useRef(false);

  const onTouchStart = useCallback(() => {
    touchedRef.current = true;
  }, []);

  const onTouchEnd = useCallback((e) => {
    // iOSÏóêÏÑú click Î∞úÏÉù Î∞©ÏßÄ
    e.preventDefault();
    onTap && onTap();
    // ÏßßÏùÄ ÏãúÍ∞Ñ ÎèôÏïà click Î¨¥Ïãú
    setTimeout(() => { touchedRef.current = false; }, 80);
  }, [onTap]);

  const onClick = useCallback((e) => {
    // ÌÑ∞ÏπòÎ°ú Ïù¥ÎØ∏ Ï≤òÎ¶¨Îêú Í≤ΩÏö∞ click Î¨¥Ïãú
    if (touchedRef.current) {
      e.preventDefault();
      return;
    }
    onTap && onTap();
  }, [onTap]);

  return { onTouchStart, onTouchEnd, onClick };
}

function TapButton({ className='', onTap, children, ...rest }) {
  const handlers = useSingleTap(onTap);
  return (
    <button
      type="button"
      className={className}
      {...handlers}
      {...rest}
    >
      {children}
    </button>
  );
}

